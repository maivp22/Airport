    private void addFliBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFliBtnActionPerformed
        // TODO add your handling code here:
        long passengerId = Long.parseLong(addFliId.getText());
        String flightId = addFliFlightSelec.getItemAt(addFliFlightSelec.getSelectedIndex());

        Passenger passenger = null;
        Flight flight = null;

        for (Passenger p : this.passengers) {
            if (p.getId() == passengerId) {
                passenger = p;
            }
        }

        for (Flight f : this.flights) {
            if (flightId.equals(f.getId())) {
                flight = f;
            }
        }

        passenger.addFlight(flight);
        flight.addPassenger(passenger);
    }//GEN-LAST:event_addFliBtnActionPerformed

